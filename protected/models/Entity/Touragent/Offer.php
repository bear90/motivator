namespace application\models\Entity\Touragent;

use application\models\entities;

class Offer
{

    private $data = null;

    public function __construct(entities\TouragentOffer $entity = null)
    {
        if (is_null($entity)) {
            $entity = new entities\TouragentOffer();
        }
        $this->data = $entity;
    }

    public function data()
    {
        return $this->data;
    }

    public function save(array $attributes = [])
    {
        if ($attributes) {
            $this->data->attributes = $attributes;
        }
        $this->data->save();

        if ($this->data->hasErrors()) {
            throw new \Exception (Tools::errorsToString($this->data->getErrors()));
        }

        return true;
    }
}
